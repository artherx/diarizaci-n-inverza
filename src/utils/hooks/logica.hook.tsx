export function diferenciaX(a: number, b: number) {
  return Math.abs(a - b);
}
export function TimeToPorcent(a: number, b: number) {
  return (a / b) * 100;
}
export function PorcentToTime(a: number, b: number) {
  return (a / 100) * b;
}
export function SecongstoMilisegundos(a: number) {
  return a * 1000;
}
export function MilisegundostoSecongs(a: number) {
  return a / 1000;
}